package Modelo;

/**
 *
 * @author DAW-B
 */
public class Trabajador {
    private String nombre, apellidos;

    public Trabajador(String nombre, String apellidos) {
        this.nombre = nombre;
        this.apellidos = apellidos;
    }

    public String getNombre() {
        return nombre;
    }

    public String getApellidos() {
        return apellidos;
    }
}
